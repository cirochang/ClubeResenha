    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            String matricula = jMatricula.getText();
            float credits_transfer =  Float.parseFloat(jCreditsTransfer.getText());
            User user_receive = controller.getUserByMatricula(matricula);
            User user_send = controller.getCurrentUser();
            if(user_send.getBillList().getTotalCredit_to_f() <= 0.009 && credits_transfer > user_send.getBillList().getTotalCredit_to_f() ){
                JOptionPane.showMessageDialog(null, "You don't have credits sufficent to transfer");

            }else if(credits_transfer <= 0.009){
                JOptionPane.showMessageDialog(null, "The transfer credits can't be bellow 0.01");
            }else if(user_receive != null && user_receive != user_send){
                controller.createBill(matricula, controller.getCurrentUser().getMatricula(), credits_transfer, "Transfer credits "+ controller.getCurrentUser().getFullName() + " to " + controller.getUserByMatricula(matricula).getFullName());
                JOptionPane.showMessageDialog(null, "Transfer CrÃ©dits complete");
                setInterface();
            }else{
                JOptionPane.showMessageDialog(null, "Invalid Matricula");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "The credits needs to be a number");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

