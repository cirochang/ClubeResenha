    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try{
            String matricula = txtMatricula.getText();
            String type = (String) cbType.getSelectedItem();
            float price = Float.parseFloat(txtSalary.getText());
            Member user = (Member) controller.getUserByMatricula(matricula, "Member");
            if(user != null){
                if(user.hasEmployeed()==false){
                    controller.addEmployee(matricula, controller.getCurrentUser().getMatricula() ,type,price);
                    JOptionPane.showMessageDialog(null, "The user has been emplooyed");
                    RestaurantOwnerGUI itemloader = new RestaurantOwnerGUI();
                    itemloader.setVisible(true);
                    this.setVisible(false);
                }
                else{
                    JOptionPane.showMessageDialog(null, "This member is already employee");
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Please put a matricula of a member correct");
            }
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Please put a price correct");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

