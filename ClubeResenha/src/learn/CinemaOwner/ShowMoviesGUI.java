    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try{
             String title = jTitle.getText();
            String genre = (String) cbGenre.getSelectedItem();
            String synopsis = jSynopsis.getText();
            String cast = jCast.getText();
            String direction = jDirection.getText();
            int classification = Integer.parseInt(jClassification.getText());
            controller.editMovie(title, cast, direction, genre, synopsis, direction, classification);
            JOptionPane.showMessageDialog(null, "The item of movie has been updated");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Please put a classification correct...");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

