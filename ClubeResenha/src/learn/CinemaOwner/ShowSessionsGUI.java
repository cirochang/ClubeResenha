    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try{
            int end_hour = Integer.parseInt((String)cbEndHH.getSelectedItem());
            int start_hour = Integer.parseInt((String)cbStartHH.getSelectedItem());
            int start_min = Integer.parseInt((String)cbStartMM.getSelectedItem());
            int end_min = Integer.parseInt((String)cbEndMM.getSelectedItem());
            String week_days = (String) cbWeekDays.getSelectedItem();
            int room = Integer.parseInt((String)cbRoom.getSelectedItem());
            String movie_name = (String) cbMovie.getSelectedItem();
            controller.editSession(movie_name, room, week_days, start_hour, start_min, end_hour, end_min);
            JOptionPane.showMessageDialog(null, "The session has been updated");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "An error has been ocorred");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        controller.deleteSession();
        try{
            session = controller.showMySession(0);
            Interface();
            JOptionPane.showMessageDialog(null, "The session has been deleted with sucess.");
        }catch(Exception e){
            CinemaOwnerGUI itemloader = new CinemaOwnerGUI();
            itemloader.setVisible(true);
            this.setVisible(false);
            JOptionPane.showMessageDialog(null, "All Sessions has been deleted");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

